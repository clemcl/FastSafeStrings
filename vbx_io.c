/**
 * vb_flush: Write the current block to disk.
 * Prefixes the block with a 4-byte Block Descriptor Word (BDW) 
 * as required by IBM Variable Blocked (VB) formats.
 */
static int vb_flush(vb_handle_t *vb) {
    if (vb->block_used == 0) return 0;

    /* BDW: 2 bytes for total block length (data + BDW), 2 bytes reserved */
    uint32_t bdw_len = vb->block_used + 4;
    uint8_t bdw[4] = { (uint8_t)(bdw_len >> 8), (uint8_t)(bdw_len & 0xFF), 0, 0 };

    /* Use low-level write for direct I/O throughput, bypassing C-stream overhead */
    if (write(vb->fp, bdw, 4) != 4) return -1;
    if (write(vb->fp, vb->block_buf, vb->block_used) != vb->block_used) return -1;

    vb->block_used = 0; // Reset buffer for next block
    return 0;
}

 

static int vb_fill_block(vb_handle_t *vb) {
    uint8_t bdw[4];
//    if (fread(bdw, 1, 4, vb->fp) != 4) return 0;
    if (read(vb->fp,bdw,4) != 4) return 0;
    uint32_t block_len = ((uint32_t)bdw[0] << 8) | (uint32_t)bdw[1];
    uint32_t payload_len = block_len - 4;
    if (payload_len > vb->block_size) return -1;
    if (read(vb->fp, vb->block_buf, payload_len) != payload_len) return -1;
//    if (fread(vb->block_buf, 1, payload_len, vb->fp) != payload_len) return -1;
    vb->block_used = payload_len;
    vb->block_pos = 0;
    return 1;
}

vb_handle_t *VB_Open(const char *path, const char *mode_str, uint32_t block_size) {
    vb_handle_t *vb = (vb_handle_t *)calloc(1, sizeof(vb_handle_t));
    if (!vb) return NULL;

    /* 1. Determine Read or Write Mode */
    if (mode_str[0] == 'w') {
        vb->fp = open(path, O_WRONLY | O_CREAT | O_TRUNC | O_BINARY, 0666);
        vb->mode = VB_MODE_WRITE;
    } else {
        vb->fp = open(path, O_RDONLY | O_BINARY);
        vb->mode = VB_MODE_READ;
    }

    if (vb->fp == -1) {
       free(vb);
       MVS_ABEND("S013", "Open Failed");
       return NULL;
    }

    /* 2. Parse Code Page / Table Requirements */
    if (strstr(mode_str, "codeset=037")) {
        vb->trans_table = (vb->mode == VB_MODE_WRITE) ? 
                           ASCII_TO_EBCDIC_037 : EBCDIC_037_TO_ASCII;
    } 
    else if (strstr(mode_str, "codeset=1047")) {
        vb->trans_table = (vb->mode == VB_MODE_WRITE) ? 
                           ASCII_TO_EBCDIC_1047 : EBCDIC_1047_TO_ASCII;
    }

    vb->block_size = block_size;
    vb->block_buf = (uint8_t *)malloc(block_size);
    return vb;
}
 
