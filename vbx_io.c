/**
 * VB_Put: Write a record to the VB buffer.
 * Handles RDW prefixing, block-overflow flushing, and high-speed translation.
 */
int VB_Put(vb_handle_t *vb, const void *data, uint32_t len) {
    /* RDW: 2 bytes for record length (data + RDW), 2 bytes reserved */
    uint32_t rdw_len = len + 4;
     
   /* If record exceeds remaining block space, flush current block first */     
    if (vb->block_used + rdw_len > vb->block_size) {
        if (vb_flush(vb) < 0) return -1;
    }

   /* Guard against records larger than the maximum block size */
    if (rdw_len > vb->block_size) {
       MVS_ABEND("S013", "Record exceeds maximum block size");
    }
 
    uint8_t *p = vb->block_buf + vb->block_used;
    
    /* Set RDW header */  
    p[0] = (uint8_t)(rdw_len >> 8); 
    p[1] = (uint8_t)(rdw_len & 0xFF);
    p[2] = 0; p[3] = 0;

    uint8_t *dest = p + 4;
    const uint8_t *src = (const uint8_t *)data;
    const uint8_t *tbl = vb->trans_table;
    uint32_t i = 0;

    /* Character Translation Logic (ASCII <-> EBCDIC) */
    if (tbl) {
        /* Unrolled by 8: Maximum Performance / Minimum Electricity */
        /* Reduces instruction overhead and branch misprediction. */
        if (len >= 8) {
            for (; i <= len - 8; i += 8) {
                dest[i]   = tbl[src[i]];   dest[i+1] = tbl[src[i+1]];
                dest[i+2] = tbl[src[i+2]]; dest[i+3] = tbl[src[i+3]];
                dest[i+4] = tbl[src[i+4]]; dest[i+5] = tbl[src[i+5]];
                dest[i+6] = tbl[src[i+6]]; dest[i+7] = tbl[src[i+7]];
            }
        }
        /* Tail loop for non-multiple-of-8 records */
        for (; i < len; i++) dest[i] = tbl[src[i]];
    } else {
        /* Raw copy using compiler intrinsic for maximum hardware speed */
        __builtin_memcpy(dest, src, len);
    }

    vb->block_used += rdw_len;
    return 1;
}
 
/**
 * VB_GetLocate: Locate a record in the buffer without copying.
 * This "Locate Mode" I/O is the fastest way to process mainframe data.
 */
int VB_GetLocate(vb_handle_t *vb, const char **ptr, uint32_t *len) {
    /* Ensure the current block has enough data for at least the length header */
    if (vb->block_pos + 4 > vb->block_used) {
        if (vb_fill_block(vb) <= 0) return 0; // EOF or Error
    }

    uint8_t *p = vb->block_buf + vb->block_pos;
    
    /* Extract Record length from RDW */
    uint32_t rdw_len = ((uint32_t)p[0] << 8) | (uint32_t)p[1];
    
    /* Safety check: ensure the record does not overrun the block buffer */
    if (rdw_len < 4 || (vb->block_pos + rdw_len > vb->block_used)) {
        MVS_ABEND("S0C4", "Record Descriptor Word (RDW) corruption detected");
        vb->error = 1;
        return -1;
    }

    /* Zero-Copy: provide pointer directly into the block buffer */
    *len = rdw_len - 4;
    *ptr = (const char *)(p + 4);
    
    /* Advance internal position to the start of the next record */
    vb->block_pos += rdw_len;
    return 1;
}
 


int VB_Get(vb_handle_t *vb, void *buf, uint32_t max_len, uint32_t *out_len) {
    const char *src_ptr;
    uint32_t rlen;

    // Locate the record in the current block
    if (VB_GetLocate(vb, &src_ptr, &rlen) <= 0) return 0;
    if (rlen > max_len) return -1;

    uint8_t *dest = (uint8_t *)buf;
    const uint8_t *src = (const uint8_t *)src_ptr;
    const uint8_t *tbl = vb->trans_table; // Now used for EBCDIC -> ASCII

    if (tbl) {
        uint32_t i = 0;
        /* Unroll by 8 for symmetry and speed */
        if (rlen >= 8) {
            for (; i <= rlen - 8; i += 8) {
                dest[i]   = tbl[src[i]];   dest[i+1] = tbl[src[i+1]];
                dest[i+2] = tbl[src[i+2]]; dest[i+3] = tbl[src[i+3]];
                dest[i+4] = tbl[src[i+4]]; dest[i+5] = tbl[src[i+5]];
                dest[i+6] = tbl[src[i+6]]; dest[i+7] = tbl[src[i+7]];
            }
        }
        for (; i < rlen; i++) dest[i] = tbl[src[i]];
    } else {
        __builtin_memcpy(dest, src, rlen); // Use the same intrinsic for raw speed
    }

    if (out_len) *out_len = rlen;
    return 1;
}


/**
 * VB_Skip: Move past the current record without copying data.
 * Maximizes performance by skipping unnecessary memory operations.
 */
int VB_Skip(vb_handle_t *vb, uint32_t *skipped_len) {
    /* 1. Ensure the current block has enough data for at least the RDW */
    if (vb->block_pos + 4 > vb->block_used) {
        if (vb_fill_block(vb) <= 0) return 0; /* EOF or Error */
    }

    /* 2. Read the record length from the RDW */
    uint8_t *p = vb->block_buf + vb->block_pos;
    uint32_t rlen = ((uint32_t)p[0] << 8) | (uint32_t)p[1];

    /* 3. Safety check: ensure record data does not overrun the block boundary */
    if (vb->block_pos + rlen > vb->block_used || rlen < 4) {
        vb->error = 1;
        return -1;
    }

    /* 4. Advance the pointer-no memcpy required, maximizing performance */
    vb->block_pos += rlen;
    
    if (skipped_len) {
        *skipped_len = rlen - 4;
    }

    return 1; /* Success */
}
 
void VB_Close(vb_handle_t *vb) {
    if (!vb || vb->fp == -1) return; // Guard against null or already closed
    if (vb->mode == VB_MODE_WRITE) vb_flush(vb);
    close(vb->fp);
 //   if (vb->fp) fclose(vb->fp);
    vb->fp = -1; // Mark as invalid
    if (vb->block_buf) {
        free(vb->block_buf);
        vb->block_buf = NULL;
    }
    free(vb);
}

